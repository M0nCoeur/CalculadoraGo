package main

func Soma(num1, num2 float64) float64 {
	resultadoSoma := num1 + num2
	return resultadoSoma
}

func Menos(num1, num2 float64) float64 {
	resultadoSub := num1 - num2
	return resultadoSub
}

func Multi(num1, num2 float64) float64 {
	resultadoMulti := num1 * num2
	return resultadoMulti
}

func Div(num1, num2 float64) float64 {
	resultadoDiv := num1 / num2
	return resultadoDiv
}